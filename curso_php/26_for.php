<?php

/*

El ciclo for permite repetir N veces un bloque de código, la diferencia es que la cantidad tiene
que estar definida en la condición y el contador viene incluído en la misma.

*/

# Declaro $i en 1 e indico que se repetirá mientras $i sea menor o igual a 20. Después de
# cada iteración se incrementa el valor de $i automáticamente.

for($i = 1; $i <= 20; $i++) { 
    echo $i . "<br/>";
}

?>